package com.automation.class4;

public class WhileLoop {
	public static void main(String[] args) {

		// Write a program to print only even number from 1 to 10

		// Initialization
		int i = 2;

		// Condition
		while (i <= 10) {
			System.out.println(i);

			// Increment / decrement
			i = i + 2;
		}

	}

}
